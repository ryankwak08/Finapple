function splitQuestionsAcrossQuizzes(questions, quizCount) {
  if (!Array.isArray(questions) || questions.length === 0 || quizCount <= 0) {
    return [];
  }

  const result = [];
  let cursor = 0;

  for (let i = 0; i < quizCount; i += 1) {
    const remainingQuestions = questions.length - cursor;
    const remainingQuizzes = quizCount - i;
    const takeCount = Math.ceil(remainingQuestions / remainingQuizzes);

    result.push(questions.slice(cursor, cursor + takeCount));
    cursor += takeCount;
  }

  return result;
}

function buildCustomTeenQuizQuestions() {
  const questionMap = {};

  teenQuizUnitsCatalog.forEach((unit) => {
    const pool = conceptCheckQuestionPoolsByUnit[unit.id];
    if (!pool?.length) {
      return;
    }

    const distributedQuestions = splitQuestionsAcrossQuizzes(pool, unit.quizzes.length);
    unit.quizzes.forEach((quiz, index) => {
      questionMap[quiz.id] = distributedQuestions[index] ?? [];
    });
  });

  return questionMap;
}

